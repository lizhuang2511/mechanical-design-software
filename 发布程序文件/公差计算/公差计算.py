def 计算间隙(基本尺寸,孔上偏差,孔下偏差,轴上偏差,轴下偏差):
    基本尺寸=基本尺寸
    孔上偏差=孔上偏差
    孔下偏差=孔下偏差
    轴上偏差=轴上偏差
    轴下偏差=轴下偏差
    轴上极限尺寸=基本尺寸+轴上偏差
    轴下极限尺寸=基本尺寸+轴下偏差
    孔上极限尺寸=基本尺寸+孔上偏差
    孔下极限尺寸=基本尺寸+孔下偏差
    最大间隙=孔上极限尺寸-轴下极限尺寸#丝
    最小间隙=孔下极限尺寸-轴上极限尺寸
    return 最大间隙,最小间隙
